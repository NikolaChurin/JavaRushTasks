package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int b =0;
        while (b<5){
            int a=0;
            while (a<10){
                System.out.print("Q");
                a++;
            }
            System.out.println();
            b++;
        }
    }
}